# 1. Write a program that checks if a given number is positive, negative, or zero.

num = int(input("Enter a Number = "))
if num < 0:
    print (f"The {num} is Negative")
elif num > 0:
    print (f"The {num} is Positive")
else:
    print (f"The {num} is ZERO")
